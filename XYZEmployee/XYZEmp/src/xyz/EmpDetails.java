    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        try {
            getConnection();
            PreparedStatement stat = con.prepareStatement("Delete EmployeeDetails where Employee_Name=? ");
            stat.setString(1, txtEmpName.getText());
            int rows = stat.executeUpdate();
            if (rows > 0) {
                JOptionPane.showMessageDialog(null, "Deleted..");
            }
            con.close();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(rootPane, "Error deleting data" + ex, "Employee Form", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        try {
            getConnection();
            PreparedStatement pst = con.prepareStatement("insert into EmployeeDetails values(?,?,?,?,?,?,?,?,?,?,?,?)");

            pst.setString(1, txtEmpId.getText());
            pst.setString(2, txtEmpName.getText());
            pst.setString(3, txtDOB.getText());
            pst.setString(4, txtCity.getText());
            pst.setString(5, txtStreet.getText());
            pst.setString(6, txtApartmentNo.getText());
            pst.setString(7, txtPhone.getText());
            pst.setString(8, txtSalary.getText());
            pst.setString(9, (String) txtGender.getSelectedItem());
            pst.setString(10, txtBankName.getText());
            pst.setString(11, txtBranchName.getText());
            pst.setString(12, txtAccNo.getText());

            int noRows = pst.executeUpdate();

            if (noRows > 0) {
                JOptionPane.showMessageDialog(rootPane, "Your details have been registered");
            } else {
                JOptionPane.showMessageDialog(rootPane, "Data is not valid", null, JOptionPane.ERROR_MESSAGE);
            }
            con.close();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(rootPane, "Error: incorrect data" + ex, null, JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btnAddActionPerformed

