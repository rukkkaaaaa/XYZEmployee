    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        try {
            getConnection();
            PreparedStatement pts = con.prepareStatement("Select * from EmployeeDetails where Employee_Name=?");
            pts.setString(1,txtEmpName.getText());
            ResultSet rs = pts.executeQuery();

            if (rs.next()) 
            {
                txtEmpId.setText(rs.getString("EmployeeID"));
                txtDOB.setText(rs.getString("DOB"));
                txtCity.setText(rs.getString("City"));
                txtStreet.setText(rs.getString("Street"));
                txtApartmentNo.setText(rs.getString("Apartment_No"));
                txtPhone.setText(rs.getString("Phone"));
                txtSalary.setText(rs.getString("Salary"));               
                txtGender.setSelectedItem(rs.getString("Gender").trim());
                
               if (txtGender.equals("Male"))
                   
          {
              
          txtGender.setSelectedItem ("Male");
                                    }
          else if(txtGender.equals("Female"))
         {
            txtGender.setSelectedItem("Female");
        }
                txtBankName.setText(rs.getString("Bank_Name"));
                txtBranchName.setText(rs.getString("Branch_Name"));
                txtAccNo.setText(rs.getString("Account_No"));

                 
                JOptionPane.showMessageDialog(null, "Employee Details found.");
                  }
            else 
            {
                JOptionPane.showMessageDialog(null,"Employee Details not found.");
            }
                 
            rs.close();
            con.close();
            }
                  
     catch (SQLException ex) {
            Logger.getLogger(EmpDetails.class.getName()).log(Level.SEVERE, null, ex);
        }       
    }//GEN-LAST:event_btnSearchActionPerformed

